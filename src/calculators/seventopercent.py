def sevenToPercent(seven):
    if seven == 7:
        return "95+"
    elif seven == 6:
        return "85-92"
    elif seven == 5:
        return "77-84"
    elif seven == 4:
        return "70-76"
    elif seven == 3:
        return "65-69"
    elif seven == 2:
        return "60-64"
    else:
        return "0-59"